package testejä;

public class Loops {

	public static void main(String[] args) {

				// Tulosta sana hei 5 kertaa
				
				//  for (laskurin nollaus ; ehto  ; laskuria kasvatetaan yhdellä)
		
				for (int laskuri = 0  ; laskuri < 5 ; laskuri++)
				{
					System.out.println("Heippa!");
				}
				
				// Tulosta numerot yhdestä viiteen
				
				for (int laskuri = 0 ; laskuri < 1 ; laskuri++)
				{
					if (laskuri == 5); {
					System.out.println(5);
					}
					if (laskuri == 16); {
					System.out.println(16);
					}
					if (laskuri == 20); {
					System.out.println(20);
					}
				}
				
	}

}
